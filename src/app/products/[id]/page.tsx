function filterByCategory(all: Product[], slug: string): Product[] {
  const meta = CATEGORY_META[slug];
  if (!meta) return [];
  if (meta.brand) return all.filter(p => p.brand === meta.brand);
  if (meta.categoryIds) return all.filter(p => meta.categoryIds!.includes(p.categoryId));
  return all;
}

export default async function Page({ params }: { params: Promise<{ id: string }> }) {
  const { id } = await params;

  const catMeta = CATEGORY_META[id];

  if (catMeta) {
    const allProducts = await getAllProducts();
    const filtered = filterByCategory(allProducts, id);
    return <CategoryView slug={id} meta={catMeta} initialProducts={filtered} />;
  }

  // Individual product page — pass all products for variants/similar
  const allProducts = await getAllProducts();
  const product = allProducts.find(p => p.id === id) ?? null;
  if (!product) notFound();
  return <ProductPageClient productId={id} initialProduct={product} initialAll={allProducts} />;
}
